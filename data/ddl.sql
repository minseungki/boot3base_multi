DROP TABLE IF EXISTS TB_BBS;
CREATE TABLE TB_BBS
(
    BBS_SEQ           BIGINT SERIAL PRIMARY KEY comment '게시판_시퀀스',
    BBS_SECTION_CD    VARCHAR(20) COMMENT '게시판_구분_코드-공지사항, FAQ, QnA',
    TITLE             VARCHAR(100) COMMENT '제목',
    CONTENTS          TEXT COMMENT '내용',
    EXPOSURE_BEGIN_DT TIMESTAMP WITH TIME ZONE COMMENT '노출_시작_일시',
    EXPOSURE_END_DT   TIMESTAMP WITH TIME ZONE COMMENT '노출_종료_일시',
    USE_YN            BOOLEAN COMMENT '사용_여부',
    EXPOSURE_YN       BOOLEAN COMMENT '노출_여부',
    REGISTER_SEQ      BIGINT COMMENT '등록자_시퀀스',
    REGISTRATION_DT   TIMESTAMP WITH TIME ZONE COMMENT '등록_일시',
    REGISTER_IP       VARCHAR(15) COMMENT '등록자_IP',
    UPDATER_SEQ       BIGINT COMMENT '수정자_시퀀스',
    UPDATE_DT         TIMESTAMP WITH TIME ZONE COMMENT '수정_일시',
    UPDATER_IP        VARCHAR(15) COMMENT '수정자_IP'
);
COMMENT ON TABLE TB_BBS IS '게시판';

DROP TABLE IF EXISTS MEMBER;
create table MEMBER
(
    MEMBER_SEQ           BIGINT SERIAL PRIMARY KEY,
    ID                   CHARACTER VARYING(20),
    PASSWORD             CHARACTER VARYING(255),
    PASSWORD_CHANGE_DT   TIMESTAMP(3) default NOW(),
    EMAIL                CHARACTER VARYING(255),
    NAME                 CHARACTER VARYING(20),
    MOBILE_PHONE_NO      CHARACTER VARYING(255),
    RECENT_LOGIN_DT      TIMESTAMP(3),
    RECENT_CONNECTION_DT TIMESTAMP(3),
    MEMBER_STATUS_CD     CHARACTER VARYING(20),
    LOGIN_FAILURE_COUNT  INTEGER   default 0,
    USE_YN               CHARACTER default 'Y',
    ROLE                 VARCHAR2(10) default 'USER' not null
);

